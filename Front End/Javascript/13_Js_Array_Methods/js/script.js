//push{Adds new elements to the end of an array }
numbers.push(50);

//pop{Removes the last element from an array }
numbers.pop();

//unshift{Adds new elements to the beginning of an array}
numbers.unshift(21);

//shift{Removes the first element from an array}
numbers.shift();